# Even or odd check using if-else
num = int(input("Enter a number: "))
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# Sum of numbers from 1 to 10 using for loop
total = 0
for i in range(1, 11):
    total += i
print("Sum:", total)

# Print first 5 even numbers using while loop
i = 2
count = 0
while count < 5:
    print(i)
    i += 2
    count += 1




