# simple arithmatic operations
a = 10
b = 20

print("Addition: ", a + b)
print("Subtraction: ", a - b)
print("Multiplication: ", a * b)
print("Division: ", a / b)
print("Modulus: ", a % b)
print("Exponentiation: ", a ** b)
print("Floor Division: ", a // b)

# variables and types
x = "python" # string
y = 3.14    # float
z = 42      # integer
print("Type of x: ", type(x))
print("Type of y: ", type(y))
print("Type of z: ", type(z))

# identify and membership operators
print(a is b) # identity operator
print("y" in "python") # membership operator

